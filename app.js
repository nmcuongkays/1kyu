function makeQuestion(item,type,mode){
  const choices = makeChoices(item, mode==="compare" ? item.confuse : null);
  if(type==="definition"){
    return {type,item,choices,prompt:item.definition, title:"Đọc định nghĩa JP và chọn tên hiện tượng"};
  }
  if(type==="counter"){
    return {type,item,choices,prompt:item.countermeasures.join(" / "), title:"Đọc đối sách và chọn hiện tượng"};
  }
  return {type:"image",item,choices,prompt:item.image,title:"Nhìn hình và chọn tên hiện tượng"};
}
function makeChoices(item, preferredNames=null){
  let wrong = DATA.filter(x=>x.id!==item.id);
  if(preferredNames && preferredNames.length){
    const prefs = preferredNames.map(n=>DATA.find(x=>x.jp===n || x.id===n || x.vi.includes(n))).filter(Boolean);
    wrong = [...prefs, ...wrong.filter(x=>!prefs.includes(x))];
  }
  const ch = shuffle([item, ...wrong.slice(0,3)]).slice(0,4);
  if(!ch.includes(item)){
    ch[0]=item; return shuffle(ch);
  }
  return ch;
}
