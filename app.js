function answerQuestion(selectedId){
  if(quiz.answered) return;
  quiz.answered = true;
  const q=quiz.questions[quiz.index];
  const ok = selectedId===q.item.id;
  if(ok) quiz.correct++;
  state.attempts++;
  if(ok) state.correct++;
  if(!state.modeStats[quiz.mode]) state.modeStats[quiz.mode]={attempts:0,correct:0};
  state.modeStats[quiz.mode].attempts++;
  if(ok) state.modeStats[quiz.mode].correct++;
  if(!ok) state.wrongIds[q.item.id]=(state.wrongIds[q.item.id]||0)+1;
  else if(state.wrongIds[q.item.id]) state.wrongIds[q.item.id]=Math.max(0,state.wrongIds[q.item.id]-1);
  Object.keys(state.wrongIds).forEach(id=>{ if(state.wrongIds[id]<=0) delete state.wrongIds[id]; });
  quiz.answers.push({id:q.item.id, selected:selectedId, ok});
  state.history.push({time:Date.now(), mode:quiz.mode, id:q.item.id, ok});
  state.history = state.history.slice(-300);
  saveState();

  $$(".choice").forEach(btn=>{
    btn.disabled=true;
    if(btn.dataset.id===q.item.id) btn.classList.add("correct");
    if(btn.dataset.id===selectedId && !ok) btn.classList.add("wrong");
  });
  const fb=$("#feedback");
  fb.className="feedback "+(ok?"ok":"bad");
  fb.innerHTML = ok ? "Đúng. Nhớ luôn hình + từ khóa này." : `Sai. Đáp án đúng là <b>${q.item.jp}</b>.`;
  $("#answerDetail").classList.remove("hidden");
  $("#answerDetail").innerHTML = `
    <h3>${q.item.jp} — ${q.item.vi}</h3>
    <p><b>Từ khóa:</b> ${q.item.keywords.map(k=>`<span class="tag">${k}</span>`).join(" ")}</p>
    <p><b>Định nghĩa:</b> ${q.item.definition}</p>
    <p><b>Mẹo:</b> ${q.item.tip}</p>
    <p><b>Dễ nhầm:</b> ${q.item.confuse.join(" / ")}</p>
  `;
  $("#nextBtn").classList.remove("hidden");
  $(".progress span").style.width = `${(quiz.index+1)/quiz.questions.length*100}%`;
}
function nextQuestion(){
  if(quiz.index < quiz.questions.length-1){
    quiz.index++; quiz.answered=false; renderQuestion();
  }else{
    renderExamResult();
  }
}
window.nextQuestion=nextQuestion;

