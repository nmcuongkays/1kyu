function startQuiz(mode, singleId=null){
  let pool = singleId ? [byId(singleId)] : [...DATA];
  let length = mode==="mixed" ? 15 : 10;
  if(mode==="wrong"){
    const ids=Object.entries(state.wrongIds||{}).sort((a,b)=>b[1]-a[1]).map(([id])=>id);
    pool = ids.map(byId).filter(Boolean);
    if(pool.length<4) pool = [...DATA];
    length=10;
  }
  if(mode==="compare"){
    pool = DATA.filter(x => ["chipping","kesson","hakuri","kousei_hasaki","nige_men_mamou","kyoukai_mamou","sukuimen_mamou","crater_mamou","netsu_kiretsu","hirou_kiretsu"].includes(x.id));
    length=10;
  }
  if(singleId) length=6;
  const questions = [];
  for(let i=0;i<length;i++){
    const item = pool[Math.floor(Math.random()*pool.length)];
    const type = singleId ? ["image","definition","counter"][i%3] : (mode==="mixed" ? ["image","definition","counter"][i%3] : (mode==="wrong" ? ["image","definition","counter"][i%3] : (mode==="compare" ? "image" : mode)));
    questions.push(makeQuestion(item,type,mode));
  }
  quiz = {mode,index:0,questions,correct:0,answered:false,answers:[]};
  setView("quiz");
  renderQuestion();
}
window.startQuiz=startQuiz;
